    public void setRecordsToTable()
    {
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/bookshop","root","");
            PreparedStatement pst = con.prepareStatement("select * from sales_product");
            ResultSet rs = pst.executeQuery();
            
            
            while(rs.next())
            {
                String sales_id = rs.getString("sales_id");
                String bname = rs.getString("bname");
                String price = rs.getString("price");
                String qty = rs.getString("qty");
                String total = rs.getString("total");
                
                
                Object[] obj = {sales_id,bname,price,qty,total};
                
                DefaultTableModel model = (DefaultTableModel)tbl_bookdata.getModel();
                model.addRow(obj);
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }   
    }
    
    
    public void search(String str)
    {
        DefaultTableModel model = (DefaultTableModel)tbl_bookdata.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        tbl_bookdata.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(str));
    }

