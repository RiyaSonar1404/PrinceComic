                      //creating new object of User class
        String name = txtname.getText();      //in this we are going to set the value with the name
        String email = txtemail.getText();
        String password = txtpass.getText();
        
        setVisible(false);                             //{connecting with login page
        new login().setVisible(true);
        
        
        try{
            //open connection
            Class.forName("com.mysql.cj.jdbc.Driver");
           
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/bookshop","root","");
            //here book_info is a database
           
            //for insert data into table
            Statement stm = con.createStatement();
           
            String sql = "INSERT INTO user(name,email,password) values('"+name+"','"+email+"','"+password+"')";            
            //EXECUTE STATEMENT
            stm.executeUpdate(sql); //here insert query apply
           
            //display message of record inserted
            JOptionPane.showMessageDialog(this, "Regiatered Successfully");
            txtname.setText("");
            txtemail.setText("");
            txtpass.setText("");
                     
            //close connection
            con.close();          
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
        
